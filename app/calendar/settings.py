"""
Модуль app.calendar.settings — настройки календаря для WorkCalendarClient.

Определяет базовые параметры работы с производственными календарями:
- допустимый диапазон лет;
- источники данных о календарных днях.

Класс CalendarSettings служит централизованным хранилищем конфигурации,
обеспечивая единообразие настроек во всём приложении.

Экспортируемые объекты:
- CalendarSettings — класс с настройками календаря.

Пример использования:
    from app.calendar.settings import CalendarSettings
    print(CalendarSettings.MIN_YEAR)  # 2025
    print(CalendarSettings.SOURCES)      # {'ConsultantPlus': 'ConsultantPlus'}

Примечания:
- Настройки являются статическими (не требуют инициализации класса).
- Для расширения списка источников достаточно дополнить словарь SOURCES.
- Диапазон лет (MIN_YEAR, MAX_YEAR) определяет область валидных запросов к календарю.
"""

class CalendarSettings:
    """
    Класс настроек календаря.

    Содержит статические атрибуты, определяющие:
    - минимальный и максимальный год для работы с календарём;
    - доступные источники данных о праздниках и рабочих днях.

    Атрибуты:
        MIN_YEAR (int): минимальный год, поддерживаемый календарём (по умолчанию 2025).
        MAX_YEAR (int): максимальный год, поддерживаемый календарём (по умолчанию 2 Desktop).
        SOURCES (dict): словарь источников данных. Ключ и значение совпадают — 
            это идентификатор источника (например, 'ConsultantPlus').
    """

    MIN_YEAR: int = 2025
    MAX_YEAR: int = 2026

    SOURCES: dict = {
        'ConsultantPlus': 'ConsultantPlus'
    }
